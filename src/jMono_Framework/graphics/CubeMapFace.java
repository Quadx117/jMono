package jMono_Framework.graphics;

/**
 * Defines the faces in a cube map for the {@link TextureCube} class.
 * 
 * @author Eric Perron (based on the XNA Framework from Microsoft and MonoGame)
 *
 */
public enum CubeMapFace
{
    /**
     * Positive X face in the cube map.
     */
    PositiveX,

    /**
     * Negative X face in the cube map.
     */
    NegativeX,

    /**
     * Positive Y face in the cube map.
     */
    PositiveY,

    /**
     * Negative Y face in the cube map.
     */
    NegativeY,

    /**
     * Positive Z face in the cube map.
     */
    PositiveZ,

    /**
     * Negative Z face in the cube map.
     */
    NegativeZ
}
